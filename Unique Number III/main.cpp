class Solution {
  public:
    int getSingle(vector<int> &arr) {
        int ans=0;
        int nm=1;
        for(int i=0;i<32;i++){
            int cb=0;
            for(int x:arr) {
                if(x&nm) cb++;
            }
            ans+=nm*(cb%3);
            nm*=2;
        }
        return ans;
    }
};


