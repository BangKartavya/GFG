class Solution{
    public:
        
    // A[]: input array
    // N: size of array
    // Function to find the maximum index difference.
    int maxIndexDiff(int a[], int n) 
    {
        int maxi = 0;
        int s = 0;
        int e = n-1;
        
        while(s<=e) {
            if(a[s]>a[e]) e--;
            else {
                maxi = max(maxi,e-s);
                e = n-1;
                s++;
            }
        }
        
        return maxi;
    }
};

